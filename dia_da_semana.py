# Pede a digitação do dia da semana
n = int(input("Digite um número: "))

# Verifica se o número atende a condição e exibe mensagem
if n == 1:
    print("Domingo")
elif n == 2:
    print("Segunda-feira")
elif n == 3:
    print("Terça-feira")
elif n == 4:
    print("Quarta-feira")
elif n == 5:
    print("Quinta-feira")
elif n == 6:
    print("Sexta-feira")
elif n == 7:
    print("Sábado")
else:
    print("Número inválido")